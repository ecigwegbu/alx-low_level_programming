#include <stdio.h>

/**
 * add - add two integers
 * @a: first number
 * @b: second number
 * Return: the sum
 */
int add(int a, int b)
{
	return (a + b);
}


/**
 * sub - subtract two integers
 * @a: first number
 * @b: second number
 * Return: the difference
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two integers
 * @a: first number
 * @b: second number
 * Return: the product
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two integers
 * @a: first number
 * @b: second number
 * Return: the quotient
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus division of two integers
 * @a: first number
 * @b: second number
 * Return: the modulus divsion result
 */
int mod(int a, int b)
{
	return (a % b);
}

